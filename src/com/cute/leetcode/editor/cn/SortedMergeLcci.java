class Solution {
    public void merge(int[] A, int m, int[] B, int n) {
        /*for (int i = 0; i < n; i++) {
            A[m+i] = B[i];
        }
        Arrays.sort(A);*/

        // åŒæŒ‡é’ˆ
        int pa = 0;
        int pb = 0;
        int[] sorted = new int[m + n];
        int cur;
        while (pa < m || pb < n) {
            if (pa == m) {
                cur = B[pb++];
            } else if (pb == n) {
                cur = A[pa++];
            } else if (A[pa] < B[pb]) {
                cur = A[pa++];
            } else {
                cur = B[pb++];
            }
            sorted[pa + pb - 1] = cur;
        }
        for (int i = 0; i < m + n; ++i) {
            A[i] = sorted[i];
        }
    }
}
